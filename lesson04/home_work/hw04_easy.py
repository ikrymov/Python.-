# Задание-1:
# Дан список, заполненный произвольными целыми числами. 
# Получить новый список, элементы которого будут
# квадратами элементов исходного списка
# [1, 2, 4, 0] --> [1, 4, 16, 0]

print()
old_ls = [1, 2, 4, 0]
new_ls = []
for el in old_ls:
    new_ls.append(el ** 2)
print("Список элементов для операций: ", old_ls)
print("Квадраты элементов этого списка:", new_ls)

# Задание-2:
# Даны два списка фруктов.
# Получить список фруктов, присутствующих в обоих исходных списках.

print()
first_fruit_list = ['банан', 'папая', 'персик', 'абрикос', 'яблоко']

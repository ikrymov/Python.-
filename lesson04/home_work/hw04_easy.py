# Задание-3:
# Дан список, заполненный произвольными числами.
# Получить список из элементов исходного, удовлетворяющих следующим условиям:
# + Элемент кратен 3
# + Элемент положительный
# + Элемент не кратен 4

print()
list = [1, 2, 3, 4, 5, 6, 7, -8, 9, 21]
new_list = [el for el in list if (el % 3) & (el > 0) & (el % 4 != 0)]
print(f"Элементы, кратеные 3, больше нуля и не кратные 4, списка: {list} это: {new_list}")
